/* --- Decode ---------------------------------------------------------------- */

fn decode_table(bytes: &[u8], table: &[u16; 128]) -> String {
    let mut s = String::with_capacity(bytes.len());
    for &b in bytes {
        if b < 0x80 {
            s.push(b as char);
        } else {
            let cp = table[(b - 0x80) as usize];
            s.push(char::from_u32(cp as u32).unwrap_or('\u{FFFD}'));
        }
    }
    s
}

/* --- Helpers --------------------------------------------------------------- */

fn charset_from_content_type(content_type: &str) -> Option<&str> {
    for param in content_type.split(';').skip(1) {
        let param = param.trim();
        if let Some(pos) = param.find('=') {
            let key = param[..pos].trim();
            if key.eq_ignore_ascii_case("charset") {
                let mut val = param[pos + 1..].trim();
                if val.starts_with('"') && val.ends_with('"') && val.len() > 1 {
                    val = &val[1..val.len() - 1];
                }
                return Some(val);
            }
        }
    }
    None
}

/* --- Main ------------------------------------------------------------------ */

pub fn decode_body(bytes: &[u8], content_type: Option<&str>) -> String {
    let charset = content_type
        .and_then(charset_from_content_type)
        .unwrap_or("utf-8");

    match charset.to_ascii_lowercase().as_str() {
        "windows-1251" | "cp1251" | "cp-1251" => decode_table(bytes, &CP1251),
        "koi8-r" | "koi8r" => decode_table(bytes, &KOI8R),
        _ => String::from_utf8_lossy(bytes).into_owned(),
    }
}
